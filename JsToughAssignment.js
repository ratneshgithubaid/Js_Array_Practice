// 1
let a = "**";

switch (a) {
  case "*":
    console.log(12 * 12);
    break;

  case "+":
    console.log(12 * 12);
    break;

  case "-":
    console.log(12 - 12);
    break;

  case "/":
    console.log(12 / 12);
    break;

  case "**":
    console.log(12 ** 12);
    break;

  default:
    console.log("Invalid Choice");
}

